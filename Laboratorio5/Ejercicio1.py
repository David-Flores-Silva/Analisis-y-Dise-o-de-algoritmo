#ejercicio1 - complejidad lineal

def complejidad_1(n):
    #en este ejercicio vemos q la complejidad del codigo es lineal en n 
    #tiene que terminar de recorrer hasta el valor de n
    for i in range(n):
        print(i+1)
    

#Caso de prueba
complejidad_1(5)

#O(n)
#seria una complejidad lineal de n, en este caso de 5

